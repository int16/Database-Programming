package scores;

public class Face
{
    private int id;
    private int noRings;
    private String description;
    private int diameter;
    private int ringWidth;
    private boolean innerTen;
    private double innerTenWidth;
    
    
    public int getId()
    {
        return id;
    }
    public void setId(int id)
    {
        this.id = id;
    }
    public int getNoRings()
    {
        return noRings;
    }
    public void setNoRings(int noRings)
    {
        this.noRings = noRings;
    }
    public String getDescription()
    {
        return description;
    }
    public void setDescription(String description)
    {
        this.description = description;
    }
    public int getDiameter()
    {
        return diameter;
    }
    public void setDiameter(int diameter)
    {
        this.diameter = diameter;
    }
    public int getRingWidth()
    {
        return ringWidth;
    }
    public void setRingWidth(int ringWidth)
    {
        this.ringWidth = ringWidth;
    }
    public boolean isInnerTen()
    {
        return innerTen;
    }
    public void setInnerTen(boolean innerTen)
    {
        this.innerTen = innerTen;
    }
    public double getInnerTenWidth()
    {
        return innerTenWidth;
    }
    public void setInnerTenWidth(double innerTenWidth)
    {
        this.innerTenWidth = innerTenWidth;
    }
    
}
